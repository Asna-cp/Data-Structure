const str1 = 'apple';
const str2 = 'banana';

// using the equality operator (==)
console.log(str1 == str2); // output: false

// using the strict equality operator (===)
console.log(str1 === str2); // output: false

// using the relational operators
console.log(str1 < str2); // output: true
console.log(str1 > str2); // output: false
console.log(str1 <= str2); // output: true
console.log(str1 >= str2); // output: false
